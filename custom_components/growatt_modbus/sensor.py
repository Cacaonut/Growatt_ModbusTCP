    def __init__(
        self,
        coordinator: GrowattModbusCoordinator,
        config_entry: ConfigEntry,
        sensor_key: str,
        sensor_def: dict[str, Any],
    ) -> None:
        """Initialize the sensor."""
        super().__init__(coordinator)

        self._config_entry = config_entry
        self._sensor_key = sensor_key
        self._sensor_def = sensor_def
        self._attr_name = sensor_def['name']
        self._attr_unique_id = f"{config_entry.entry_id}_{sensor_key}"

        # Determine which device this sensor belongs to
        self._device_type = get_device_type_for_sensor(sensor_key)

        # Set entity category (None for main sensors, "diagnostic" for technical details)
        entity_category = get_entity_category(sensor_key)
        if entity_category == "diagnostic":
            self._attr_entity_category = EntityCategory.DIAGNOSTIC

        # Set sensor attributes based on definition
        if "device_class" in sensor_def:
            self._attr_device_class = sensor_def["device_class"]
        if "state_class" in sensor_def:
            self._attr_state_class = sensor_def["state_class"]
        if "unit" in sensor_def:
            self._attr_native_unit_of_measurement = sensor_def["unit"]
        if "icon" in sensor_def:
            self._attr_icon = sensor_def["icon"]
        if sensor_def.get("disabled_by_default"):
            self._attr_entity_registry_enabled_default = False

    @property
    def device_info(self) -> dict[str, Any]:
        """Return device information."""
        return self.coordinator.get_device_info(self._device_type)

    @property
    def available(self) -> bool:
        """Entity is available only when coordinator succeeded AND inverter is online.

        When the inverter stops responding to Modbus (truly offline), all entities
        go unavailable. HA's statistics engine ignores unavailable states, preventing
        flatline data from being recorded as real measurements. When the inverter is
        online but dormant (responds with zeros), available stays True and the
        coordinator's retention logic protects total_increasing sensors.
        """
        return self.coordinator.last_update_success and self.coordinator.is_online

    @property
    def native_value(self) -> Any:
        """Return the state of the sensor."""
        if self.coordinator.data is None:
            return None
        
        data = self.coordinator.data
        
        # Get invert grid power option
        invert_grid_power = self._config_entry.options.get(CONF_INVERT_GRID_POWER, False)
        inverter_series = self._config_entry.data.get(CONF_INVERTER_SERIES, "").lower()
        is_sph_family = inverter_series.startswith("sph_")
        
        # Special handling for calculated sensors
        if self._sensor_def.get("attr") == "calculated":
            if self._sensor_key == "grid_power":
                # Bidirectional grid power (signed value)
                # Positive = export, Negative = import
                # Per VPP V2.03: power_to_grid = "Total reverse power (grid export)"
                # Per VPP V2.03: power_to_user = "Total forward power (grid import)"
                export = getattr(data, "power_to_grid", 0)
                import_power = getattr(data, "power_to_user", 0)

                # Use direct register values when available (more accurate than calculation)
                if export > 0:
                    # Exporting to grid (positive)
                    grid_power = export
                elif import_power > 0:
                    # Importing from grid (negative)
                    grid_power = -import_power
                else:
                    # Fallback calculation when no direct register values
                    solar = getattr(data, "pv_total_power", 0)
                    load = getattr(data, "power_to_load", 0)
                    charge = getattr(data, "charge_power", 0)
                    discharge = getattr(data, "discharge_power", 0)
                    grid_power = (solar + discharge) - (load + charge)

                # Apply inversion if configured (CT clamp backwards)
                if invert_grid_power:
                    grid_power = -grid_power

                raw_value = round(grid_power, 1)

                # Apply offline behavior
                return self.coordinator.get_sensor_value(self._sensor_key, raw_value)

            elif self._sensor_key == "grid_export_power":
                # Export power (always positive or 0)
                # Per VPP V2.03: power_to_grid = "Total reverse power (grid export)"
                # Per VPP V2.03: power_to_user = "Total forward power (grid import)"
                export = getattr(data, "power_to_grid", 0)
                import_power = getattr(data, "power_to_user", 0)

                # Compute raw signed grid power (positive = export) from physical registers.
                # invert_grid_power is a sign-convention correction for the signed grid_power
                # sensor only — do NOT apply it here. The always-positive sensors derive from
                # the directional register values directly, regardless of sign convention.
                if export > 0:
                    grid_power = export
                elif import_power > 0:
                    grid_power = -import_power
                else:
                    solar = getattr(data, "pv_total_power", 0)
                    load = getattr(data, "power_to_load", 0)
                    charge = getattr(data, "charge_power", 0)
                    discharge = getattr(data, "discharge_power", 0)
                    grid_power = (solar + discharge) - (load + charge)

                # Export = positive portion of raw signed value
                raw_value = round(max(0, grid_power), 1)
                return self.coordinator.get_sensor_value(self._sensor_key, raw_value)

            elif self._sensor_key == "grid_import_power":
                # Import power (always positive or 0)
                # Per VPP V2.03: power_to_user = "Total forward power (grid import)"
                # Per VPP V2.03: power_to_grid = "Total reverse power (grid export)"
                export = getattr(data, "power_to_grid", 0)
                import_power = getattr(data, "power_to_user", 0)

                # Same raw derivation — no inversion applied (see grid_export_power comment).
                if export > 0:
                    grid_power = export
                elif import_power > 0:
                    grid_power = -import_power
                else:
                    solar = getattr(data, "pv_total_power", 0)
                    load = getattr(data, "power_to_load", 0)
                    charge = getattr(data, "charge_power", 0)
                    discharge = getattr(data, "discharge_power", 0)
                    grid_power = (solar + discharge) - (load + charge)

                # Import = negative portion of raw signed value, flipped to positive
                raw_value = round(max(0, -grid_power), 1)
                return self.coordinator.get_sensor_value(self._sensor_key, raw_value)
                
            elif self._sensor_key == "self_consumption":
                # Self consumption = solar used directly
                solar = getattr(data, "pv_total_power", 0)
                export = getattr(data, "power_to_grid", 0)
                load = getattr(data, "power_to_load", 0)
                
                if export > 0:
                    self_consumption = solar - export
                elif load > 0:
                    self_consumption = min(solar, load)
                else:
                    self_consumption = solar
                
                raw_value = round(max(0, self_consumption), 1)
                return self.coordinator.get_sensor_value(self._sensor_key, raw_value)
            
            elif self._sensor_key == "self_consumption_percentage":
                # Percentage of solar self-consumed
                solar = getattr(data, "pv_total_power", 0)
                export = getattr(data, "power_to_grid", 0)
                load = getattr(data, "power_to_load", 0)
                
                if solar == 0:
                    raw_value = 0
                else:
                    if export > 0:
                        self_consumption = solar - export
                    elif load > 0:
                        self_consumption = min(solar, load)
                    else:
                        self_consumption = solar
                    
                    percentage = (max(0, self_consumption) / solar) * 100
                    raw_value = round(percentage, 1)
                
                return self.coordinator.get_sensor_value(self._sensor_key, raw_value)
                
            elif self._sensor_key == "house_consumption":
                # House consumption
                load = getattr(data, "power_to_load", 0)

                if load == 0:
                    # Energy balance: solar + battery_discharge - battery_charge + grid_import - grid_export
                    # self_consumption_power (reg 1037/1038 on HU) is NOT used — it reports
                    # solar self-consumed, not total house load (omits battery discharge + grid import).
                    solar = getattr(data, "pv_total_power", 0)
                    export = getattr(data, "power_to_grid", 0)
                    import_power = getattr(data, "power_to_user", 0)
                    charge = getattr(data, "charge_power", 0)
                    discharge = getattr(data, "discharge_power", 0)
                    has_battery = (charge > 0 or discharge > 0)
                    has_grid = (export > 0 or import_power > 0)
                    if has_battery or has_grid:
                        load = solar + discharge - charge + import_power - export
                    else:
                        load = max(0, solar - export)

                raw_value = round(max(0, load), 1)
                return self.coordinator.get_sensor_value(self._sensor_key, raw_value)
            
            elif self._sensor_key == "battery_power":
                # Battery power: positive = charging, negative = discharging
                charge_power = getattr(data, "charge_power", 0)
                discharge_power = getattr(data, "discharge_power", 0)
                
                if charge_power > 0:
                    raw_value = round(charge_power, 1)  # Positive for charging
                elif discharge_power > 0:
                    raw_value = round(-discharge_power, 1)  # Negative for discharging
                else:
                    raw_value = 0
                
                return self.coordinator.get_sensor_value(self._sensor_key, raw_value)
            
            
            elif self._sensor_key == "grid_energy_today":
                # Combined grid energy: positive=export, negative=import
                # Net grid energy = export - import
                export_energy = getattr(data, "energy_to_grid_today", 0)
                load_energy = getattr(data, "load_energy_today", 0)
                solar_energy = getattr(data, "energy_today", 0)
                
                # Import = load - solar + export
                import_energy = max(0, load_energy - solar_energy + export_energy)
                
                # Net: positive when more export than import
                net_energy = export_energy - import_energy
                
                # Apply inversion if configured
                if invert_grid_power:
                    net_energy = -net_energy
                
                raw_value = round(net_energy, 2)
                return self.coordinator.get_sensor_value(self._sensor_key, raw_value)
            
            elif self._sensor_key == "grid_energy_total":
                # Combined grid energy: positive=export, negative=import
                # Net grid energy = export - import
                export_energy = getattr(data, "energy_to_grid_total", 0)
                load_energy = getattr(data, "load_energy_total", 0)
                solar_energy = getattr(data, "energy_total", 0)
                
                # Import = load - solar + export
                import_energy = max(0, load_energy - solar_energy + export_energy)
                
                # Net: positive when more export than import
                net_energy = export_energy - import_energy
                
                # Apply inversion if configured
                if invert_grid_power:
                    net_energy = -net_energy
                
                raw_value = round(net_energy, 2)
                return self.coordinator.get_sensor_value(self._sensor_key, raw_value)

            elif self._sensor_key == "grid_import_energy_today":
                # Grid import energy - use hardware register if available, otherwise calculate
                # SPH/SPH-TL3: energy_to_user_today (hardware bidirectional meter = grid import)
                # Other models: energy_from_grid_today

                # Check if inverter has hardware import energy register
                # SPH family uses "energy_to_user" for import, while non-SPH models
                # should not treat energy_to_user as grid import
                has_hardware_import = hasattr(data, "energy_from_grid_today") or (
                    is_sph_family and hasattr(data, "energy_to_user_today")
                )

                if has_hardware_import:
                    # Hardware energy registers are accumulated by the inverter's internal
                    # bidirectional power meter, independent of CT clamp orientation.
                    # energy_to_user_today always correctly measures grid import regardless
                    # of CT direction — do NOT swap based on invert_grid_power (Issue #211).
                    raw_value = getattr(data, "energy_to_user_today", 0) if hasattr(data, "energy_to_user_today") else getattr(data, "energy_from_grid_today", 0)
                else:
                    # No hardware import register - must calculate
                    # This calculation works for non-hybrid inverters (MIN, etc.)
                    load_energy = getattr(data, "load_energy_today", 0)
                    solar_energy = getattr(data, "energy_today", 0)
                    export_energy = getattr(data, "energy_to_grid_today", 0)
                    raw_value = max(0, load_energy - solar_energy + export_energy)

                raw_value = round(raw_value, 2)
                return self.coordinator.get_sensor_value(self._sensor_key, raw_value)

            elif self._sensor_key == "grid_import_energy_total":
                # Grid import energy - use hardware register if available, otherwise calculate
                # SPH/SPH-TL3: energy_to_user_total (hardware bidirectional meter = grid import)
                # Other models: energy_from_grid_total

                # Check if inverter has hardware import energy register
                # SPH family uses "energy_to_user" for import, while non-SPH models
                # should not treat energy_to_user as grid import
                has_hardware_import = hasattr(data, "energy_from_grid_total") or (
                    is_sph_family and hasattr(data, "energy_to_user_total")
                )

                if has_hardware_import:
                    # Hardware energy registers are accumulated by the inverter's internal
                    # bidirectional power meter, independent of CT clamp orientation.
                    # energy_to_user_total always correctly measures grid import regardless
                    # of CT direction — do NOT swap based on invert_grid_power (Issue #211).
                    raw_value = getattr(data, "energy_to_user_total", 0) if hasattr(data, "energy_to_user_total") else getattr(data, "energy_from_grid_total", 0)
                else:
                    # No hardware import register - must calculate
                    # This calculation works for non-hybrid inverters (MIN, etc.)
                    load_energy = getattr(data, "load_energy_total", 0)
                    solar_energy = getattr(data, "energy_total", 0)
                    export_energy = getattr(data, "energy_to_grid_total", 0)
                    raw_value = max(0, load_energy - solar_energy + export_energy)

                raw_value = round(raw_value, 2)
                return self.coordinator.get_sensor_value(self._sensor_key, raw_value)
            
            elif self._sensor_key == "last_update":
                # Get the last successful update time from coordinator
                if hasattr(self.coordinator, 'last_update_success_time') and self.coordinator.last_update_success_time:
                    # Make sure datetime is timezone-aware (UTC)
                    dt = self.coordinator.last_update_success_time
                    if dt.tzinfo is None:
                        # If naive datetime, assume UTC and make it aware
                        dt = dt.replace(tzinfo=timezone.utc)
                    return dt
                return None
            
            return None

        # MIN TL-XH energy_today register (3049/3050) can represent total system AC
        # output and include battery discharge. For solar reporting, derive PV-only
        # daily energy from energy balance terms.
        if self._sensor_key == "energy_today" and inverter_series.startswith("min_tl_xh_"):
            load_energy = getattr(data, "load_energy_today", 0.0)
            grid_import = getattr(data, "energy_to_user_today", 0.0)
            grid_export = getattr(data, "energy_to_grid_today", 0.0)
            battery_charge = getattr(data, "charge_energy_today", 0.0)
            battery_discharge = getattr(data, "discharge_energy_today", 0.0)

            pv_energy_today = load_energy + battery_charge + grid_export - grid_import - battery_discharge
            pv_energy_today = round(max(0.0, pv_energy_today), 2)

            return self.coordinator.get_sensor_value(self._sensor_key, pv_energy_today)
        
        # Regular sensor - get value from data attribute
        value = getattr(data, self._sensor_def["attr"], None)
        
        if value is None:
            return None
        
        # Special handling for status sensor
        if self._sensor_key == "status":
            from .const import STATUS_CODES, HYBRID_STATUS_CODES, SPF_STATUS_CODES, PROFILE_STATUS_MAP

            if not self.coordinator.is_online:
                return "Offline"

            client = self.coordinator.modbus_client
            map_key = PROFILE_STATUS_MAP.get(client.register_map_name if client else '', 'grid_tied')
            codes = HYBRID_STATUS_CODES if map_key == 'hybrid' else (SPF_STATUS_CODES if map_key == 'spf' else STATUS_CODES)
            return codes.get(int(value), {"name": f"Unknown ({value})"})["name"]

        # Special handling for derating_mode sensor
        if self._sensor_key == "derating_mode":
            from .const import get_derating_name
            return get_derating_name(int(value))

        # Apply value map if defined (returns named string instead of raw integer)
        if "value_map" in self._sensor_def:
            return self._sensor_def["value_map"].get(int(value), f"Unknown ({value})")

        # Round numeric values to reasonable precision
        if isinstance(value, float):
            if self._sensor_def.get("unit") == UnitOfPower.WATT:
                raw_value = round(value, 1)
            elif self._sensor_def.get("unit") == UnitOfElectricPotential.VOLT:
                raw_value = round(value, 1)
            elif self._sensor_def.get("unit") == UnitOfElectricCurrent.AMPERE:
                raw_value = round(value, 2)
            elif self._sensor_def.get("unit") == UnitOfFrequency.HERTZ:
                raw_value = round(value, 2)
            elif self._sensor_def.get("unit") == UnitOfTemperature.CELSIUS:
                raw_value = round(value, 1)
            elif self._sensor_def.get("unit") == UnitOfEnergy.KILO_WATT_HOUR:
                raw_value = round(value, 2)
            else:
                raw_value = value
        else:
            raw_value = value
        
        # Apply offline behavior for this sensor
        return self.coordinator.get_sensor_value(self._sensor_key, raw_value)

    @property
    def extra_state_attributes(self) -> dict[str, Any] | None:
        """Return additional state attributes."""
        desc = self._sensor_def.get("description")
        if desc:
            return {"description": desc}
        return None
