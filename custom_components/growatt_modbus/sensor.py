def _pv_string_sensors(n: int) -> dict:
    """Return the three real-time measurement sensors for PV string N."""
    return {
        f"pv{n}_voltage": {
            "name": f"PV{n} Voltage",
            "icon": "mdi:lightning-bolt",
            "device_class": SensorDeviceClass.VOLTAGE,
            "state_class": SensorStateClass.MEASUREMENT,
            "unit": UnitOfElectricPotential.VOLT,
            "attr": f"pv{n}_voltage",
        },
        f"pv{n}_current": {
            "name": f"PV{n} Current",
            "icon": "mdi:current-dc",
            "device_class": SensorDeviceClass.CURRENT,
            "state_class": SensorStateClass.MEASUREMENT,
            "unit": UnitOfElectricCurrent.AMPERE,
            "attr": f"pv{n}_current",
        },
        f"pv{n}_power": {
            "name": f"PV{n} Power",
            "icon": "mdi:solar-panel",
            "device_class": SensorDeviceClass.POWER,
            "state_class": SensorStateClass.MEASUREMENT,
            "unit": UnitOfPower.WATT,
            "attr": f"pv{n}_power",
        },
    }


def _phase_sensors(phase: str) -> dict:
    """Return voltage, current, and power sensors for a 3-phase AC phase (r/s/t)."""
    p = phase.lower()
    P = phase.upper()
    return {
        f"ac_voltage_{p}": {
            "name": f"AC Voltage {P}",
            "icon": "mdi:lightning-bolt",
            "device_class": SensorDeviceClass.VOLTAGE,
            "state_class": SensorStateClass.MEASUREMENT,
            "unit": UnitOfElectricPotential.VOLT,
            "attr": f"ac_voltage_{p}",
        },
        f"ac_current_{p}": {
            "name": f"AC Current Phase {P}",
            "icon": "mdi:current-ac",
            "device_class": SensorDeviceClass.CURRENT,
            "state_class": SensorStateClass.MEASUREMENT,
            "unit": UnitOfElectricCurrent.AMPERE,
            "attr": f"ac_current_{p}",
        },
        f"ac_power_{p}": {
            "name": f"AC Power Phase {P}",
            "icon": "mdi:power-plug",
            "device_class": SensorDeviceClass.POWER,
            "state_class": SensorStateClass.MEASUREMENT,
            "unit": UnitOfPower.WATT,
            "attr": f"ac_power_{p}",
        },
    }


def _line_voltage_sensors(pair: str) -> dict:
    """Return a line-to-line voltage diagnostic sensor for the given pair (rs/st/tr)."""
    p = pair.lower()
    P = pair.upper()
    return {
        f"ac_voltage_{p}": {
            "name": f"AC Voltage {P}",
            "icon": "mdi:lightning-bolt",
            "device_class": SensorDeviceClass.VOLTAGE,
            "state_class": SensorStateClass.MEASUREMENT,
            "unit": UnitOfElectricPotential.VOLT,
            "attr": f"ac_voltage_{p}",
            "entity_category": EntityCategory.DIAGNOSTIC,
        },
    }


